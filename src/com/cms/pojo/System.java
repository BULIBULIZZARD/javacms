package com.cms.pojo;

public class System {

	private int id;
	private String webname;
	private int frontpagesize;
	private int pagesize;
	private int navnum;
	private int linknum;
	private int advernum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWebname() {
		return webname;
	}
	public void setWebname(String webname) {
		this.webname = webname;
	}
	public int getFrontpagesize() {
		return frontpagesize;
	}
	public void setFrontpagesize(int frontpagesize) {
		this.frontpagesize = frontpagesize;
	}
	public int getPagesize() {
		return pagesize;
	}
	public void setPagesize(int pagesize) {
		this.pagesize = pagesize;
	}
	public int getNavnum() {
		return navnum;
	}
	public void setNavnum(int navnum) {
		this.navnum = navnum;
	}
	public int getLinknum() {
		return linknum;
	}
	public void setLinknum(int linknum) {
		this.linknum = linknum;
	}
	public int getAdvernum() {
		return advernum;
	}
	public void setAdvernum(int advernum) {
		this.advernum = advernum;
	}
	
}
